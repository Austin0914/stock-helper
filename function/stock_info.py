def caluate_date():
    # 從 yfinance 獲取日期資料並儲存在變數中
    opendate_FromToday = call_yfinance(ticker="0050.TW").index.strftime('%Y-%m-%d')[::-1][:10]
    return opendate_FromToday

def caluate_investmentbanks_avgBS(opendate_FromToday, all_data):
    # 在 all_data 中新增欄位，直接進行計算
    all_data["前9日加總買賣超"] = 0
    all_data["當日投信買賣超"] = 0

    for i in range(len(opendate_FromToday)):
        time_today = opendate_FromToday[i].replace('-', '')
        index_str = "當日投信買賣超" if i == 0 else "前9日加總買賣超"
        
        # 從 TWSE 下載資料並儲存在變數中
        byorsell_data_twse = download(TWSE_investmentbanks.format(TIME=time_today), filetype="json")
        for j, company_name in enumerate(byorsell_data_twse[byorsell_data_twse.columns[1]]):
            stock_code = str(company_name).strip()
            matching_rows = all_data[all_data['股票代號'].astype(str).str.strip() == stock_code]
            if not matching_rows.empty:
                index_val = matching_rows.index[0]
                all_data.loc[index_val, index_str] += round(int(str(byorsell_data_twse[byorsell_data_twse.columns[5]][j]).replace(',', '')) / 1000)

        # 從 TPEX 下載買超資料並儲存在變數中
        byorsell_data_tpex_buy = download(TPEX_investmentbanks.format(TIME=time_today.replace('-', '%2F'), BUYorSELL="buy"), filetype="csv")
        for j, company_name in enumerate(byorsell_data_tpex_buy[byorsell_data_tpex_buy.columns[2]]):
            stock_code = str(company_name).strip()[:4]
            matching_rows = all_data[all_data['股票代號'].astype(str).str.strip() == stock_code]
            if not matching_rows.empty:
                index_val = matching_rows.index[0]
                all_data.loc[index_val, index_str] += int(float(str(byorsell_data_tpex_buy[byorsell_data_tpex_buy.columns[6]][j]).replace(',', '')))

        # 從 TPEX 下載賣超資料並儲存在變數中
        byorsell_data_tpex_sell = download(TPEX_investmentbanks.format(TIME=time_today.replace('-', '%2F'), BUYorSELL="sell"), filetype="csv")
        for j, company_name in enumerate(byorsell_data_tpex_sell[byorsell_data_tpex_sell.columns[2]]):
            stock_code = str(company_name).strip()[:4]
            matching_rows = all_data[all_data['股票代號'].astype(str).str.strip() == stock_code]
            if not matching_rows.empty:
                index_val = matching_rows.index[0]
                all_data.loc[index_val, index_str] += int(float(str(byorsell_data_tpex_sell[byorsell_data_tpex_sell.columns[6]][j]).replace(',', '')))

    return all_data

def get_rail_data(opendate_FromToday, all_data):
    # 直接在記憶體中計算並更新 all_data
    all_data = caluate_investmentbanks_avgBS(opendate_FromToday, all_data)
    return all_data

def call_yfinance(ticker="", howlongfromToday="1mo"):
    # 從 yfinance 下載資料並儲存在變數中
    data = yf.Tickers(ticker).history(period=howlongfromToday)
    return data

def get_today_price(source="web", all_data=None):
    if source == "web":
        # 從 TWSE 下載今日價格並儲存在變數中
        response = requests.get(TWSE_price_web)
        response.raise_for_status()
        json_data = response.json()
        df_twse = pd.DataFrame(json_data["data"], columns=json_data["fields"])

        # 從 TPEX 下載今日價格並儲存在變數中
        response = requests.get(TPEX_price_web)
        response.raise_for_status()
        table = response.json()["tables"][0]
        df_tpex = pd.DataFrame(table["data"], columns=table["fields"])

    # 在 all_data 中新增今日價格欄位
    all_data["今日價格"] = None

    for i, company_name in enumerate(all_data[all_data.columns[0]]):
        if all_data[all_data.columns[2]][i] == 1:  # 上櫃
            stock_code = str(company_name).strip()
            matching_rows = df_tpex[df_tpex['代號'].astype(str).str.strip() == stock_code]
            if not matching_rows.empty:
                index_val = matching_rows.index[0]
                all_data.loc[i, "今日價格"] = df_tpex.loc[index_val, '收盤']
        else:  # 上市
            stock_code = str(company_name).strip()
            matching_rows = df_twse[df_twse['證券代號'].astype(str).str.strip() == stock_code]
            if not matching_rows.empty:
                index_val = matching_rows.index[0]
                all_data.loc[i, "今日價格"] = df_twse.loc[index_val, "收盤價"]

    return all_data

def download(url="", filetype=""):
    # 下載資料並直接返回變數
    try:
        if filetype == "json":
            response = requests.get(url)
            response.raise_for_status()
            json_data = response.json()
            df = pd.DataFrame(json_data["data"], columns=json_data["fields"])
            df = df.drop(df.columns[0], axis=1)
        elif filetype == "csv":
            response = requests.get(url)
            response.raise_for_status()
            df = pd.read_csv(StringIO(response.text), skiprows=1)
            df = df[:-1]
            df.reset_index(drop=True, inplace=True)
        else:
            raise ValueError("請輸入正確的檔案格式")
    except Exception as e:
        logging.info('Error Downloading Data: ' + url)
    return df

def updateCompany():
    # 從網站下載公司資料並儲存在變數中
    twse_company_url = 'https://isin.twse.com.tw/isin/C_public.jsp?strMode=2'
    tpex_company_url = 'https://isin.twse.com.tw/isin/C_public.jsp?strMode=4'

    response = requests.get(twse_company_url)
    response.raise_for_status()
    html_content_twse = response.text

    response = requests.get(tpex_company_url)
    response.raise_for_status()
    html_content_tpex = response.text

    results_tpex = []
    soup = BeautifulSoup(html_content_tpex, 'html.parser')
    jud = False
    for row in soup.find_all('tr'):
        text = row.find('td').get_text(strip=True)
        if text in ('股票', '特別股'):
            jud ^= True
            continue
        if text and jud:
            temp = text.split("\u3000")
            results_tpex.append([temp[0], temp[1], 1])

    results_twse = []
    soup = BeautifulSoup(html_content_twse, 'html.parser')
    for i, row in enumerate(soup.find_all('tr')):
        text = row.find('td').get_text(strip=True)
        if text and text == '上市認購(售)權證':
            break
        if text and i > 1:
            temp = text.split("\u3000")
            results_twse.append([temp[0], temp[1], 0])

    final = results_tpex + results_twse
    columns = ["股票代號", "公司名稱", "上市/櫃"]
    df = pd.DataFrame(final, columns=columns)
    return df

